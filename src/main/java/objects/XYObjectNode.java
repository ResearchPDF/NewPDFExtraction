package objects;

public class XYObjectNode {
    private Node mainObject;
    private Node rightObject;
    private Node BottomObject;

    public Node getMainObject() {
        return mainObject;
    }

    public void setMainObject(Node mainObject) {
        this.mainObject = mainObject;
    }

    public Node getRightObject() {
        return rightObject;
    }

    public void setRightObject(Node rightObject) {
        this.rightObject = rightObject;
    }

    public Node getBottomObject() {
        return BottomObject;
    }

    public void setBottomObject(Node bottomObject) {
        BottomObject = bottomObject;
    }
}
